<script>
    import Cell from "./cell.svelte";
    import Icon from "./icon.svelte";
    import Priority from "./priority.svelte";
    import { createEventDispatcher } from "svelte";

    export let todo;
    const dispatch = createEventDispatcher();

    const toggle_status = () => {
        todo.done = !todo.done
    }
    const item_change = (type) => {
        dispatch ('change', {type: type, id:todo.id});
    }

    const edit_task = () => {
        document.getElementById(todo.id).blur()
    }
</script>

<Cell>
    {todo.id}
</Cell>
<Cell>
    <Icon nome={!todo.done ? "circle" : "task_alt"} handler={toggle_status} color="red"/>
</Cell>
<Cell>
    <input type="text"
            class="todo-item-input-text {todo.done == true ? 'text-done' : ''}"
            id={todo.id}
            placeholder="Inserisci il nuovo ToDo"
            bind:value={todo.task} 
            on:change={edit_task} />
</Cell>
<Cell>
    <Priority disabled={todo.done}/>
</Cell>
<Cell last>
    <Icon nome="delete_forever" color="red" handler={() => item_change('delete')}/>
</Cell>

<style>
    .todo-item-input-text {
        border: none;
        width: 90%;
        height: 30px;
        font-size: 20px;
        color: #525252;
    }

    .todo-item-input-text:focus {
        background-color: rgb(204, 229, 253);
